# operadores: > | < | >= | <= | != | ==

temperatura = 38

if temperatura > 34:
    print('O dia está muito quente.')
    print ('Beba bastante água!')

if temperatura <= 10:
    print('O dia está muito frio.')
    print('Agasalhe-se')

if temperatura == -15:    
    print('Proteja-se!')

if temperatura != 0:
    print('Tenha um bom dia!')




