# While:
# Estrutura de repetição (loop)
# Enquanto a condição é verdadeira, ele executa o loop
# while condição:
    #bloco de código

i = 1
while i <= 5:
    print(i)
    i = i + 1
print('Terminou!')

#Funcionamento:
# i = 1 → imprime 1
# incrementa → i = 2
# verifica → 2 <= 5 → imprime 2
# continua até i = 5

i = 1
while i <= 10:
    print('*' * i)
    i = i + 1
print('Terminou!')
